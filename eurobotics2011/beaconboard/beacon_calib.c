/* get distance (cm) from relative size */
uint16_t get_dist_array(int32_t size, int32_t period)
{
	uint32_t measure;
	uint16_t index;

	/* calculate measure */
	measure = (uint32_t)((size*100000)/period);

	/* saturate to working range */
	if(measure > MEASURE_MAX)
		measure = MEASURE_MAX;
	if(measure < MEASURE_MIN)
		measure = MEASURE_MIN;

	/* calculate array index */
	index = (ARRAY_SIZE-1) - ((measure-X_EVAL_MIN)/X_EVAL_DELTA);	

	//BEACON_DEBUG("measure = %.4ld / index = %.3d / dist = %.3d cm",
	//		       measure, index, measure2distance[index]);

	/* return distance from array in mm */
	return (measure2distance[index]*10);

}
